
package pacman.komponentit;

public class Hedelma {
    private int x;
    private int y;
    private int arvo;
    
    public Hedelma(int x, int y) {
        this.x = x;
        this.y = y;
        this.arvo = 100;
    }
    
    public int getArvo() {
        return this.arvo;
    }
    
    public int getX() {
        return this.x;
    }
    
    public int getY() {
        return this.y;
    }
    
    
    
}
